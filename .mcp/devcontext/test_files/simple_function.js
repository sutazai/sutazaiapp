/**
 * A simple function for testing importance score calculation.
 * This is a basic example to test the simpler case of entity indexing.
 */

/**
 * Adds two numbers and returns the result
 * @param {number} a - First number
 * @param {number} b - Second number
 * @returns {number} Sum of a and b
 */
function add(a, b) {
  return a + b;
}

/**
 * Subtracts second number from first and returns the result
 * @param {number} a - First number
 * @param {number} b - Second number to subtract
 * @returns {number} Result of a - b
 */
function subtract(a, b) {
  return a - b;
}

module.exports = {
  add,
  subtract,
};
